#Напишіть цикл, який буде вимагати від користувача ввести слово, в якому
# є літера "h" (враховуються як великі так і маленькі). Цикл не повинен завершитися,
# якщо користувач ввів слово без букви "h".
i=1
while i>0:
    input_word = input("Enter word:")
    index1 = input_word.find("H")
    index2 = input_word.find("h")
    if index1 >=0 or index2 >=0:
        i=0
    else:
        i+=1